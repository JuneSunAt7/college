'''
Относительное отклонение для первых трех строк.

Относительное отклонение= 
Базисный год / Отчетный год−Базисный год ×100

Фондовооруженность (руб) для четвертой строки.

Фондовооруженность= 
Среднегодовая численность персонала / Среднегодовая стоимость основных производственных средств
 
Техническая вооруженность труда (руб) для пятой строки.

Техническая вооруженность труда= 
Среднегодовая численность персонала / Среднегодовая стоимость активной части основных средств

'''''
data = {
    "Показатель": [
        "Среднегодовая стоимость основных производственных средств (тыс. руб)",
        "Среднегодовая стоимость активной части основных средств (тыс. руб)",
        "Среднегодовая численность промышленно-производственного персонала (чел)",
        "Фондовооруженность (руб)",
        "Техническая вооруженность труда (руб)"
    ],
    "Базисный год": [148306, 103684, 972, None, None],
    "Отчетный год": [148525, 104549, 1336, None, None],
    "Относительное отклонение": [None, None, None, None, None]
}

# относительое отклонение
for i in range(3):  # первые три строки
    base_value = data["Базисный год"][i]
    report_value = data["Отчетный год"][i]
    relative_deviation = ((report_value - base_value) / base_value) * 100
    data["Относительное отклонение"][i] = round(relative_deviation, 2)

# фондовооруженность
base_fund_per_worker = data["Базисный год"][0] / data["Базисный год"][2]
report_fund_per_worker = data["Отчетный год"][0] / data["Отчетный год"][2]
data["Базисный год"][3] = round(base_fund_per_worker, 2)
data["Отчетный год"][3] = round(report_fund_per_worker, 2)
data["Относительное отклонение"][3] = round(
    ((report_fund_per_worker - base_fund_per_worker) / base_fund_per_worker) * 100, 2
)

# тех вооруженность труда
base_tech_per_worker = data["Базисный год"][1] / data["Базисный год"][2]
report_tech_per_worker = data["Отчетный год"][1] / data["Отчетный год"][2]
data["Базисный год"][4] = round(base_tech_per_worker, 2)
data["Отчетный год"][4] = round(report_tech_per_worker, 2)
data["Относительное отклонение"][4] = round(
    ((report_tech_per_worker - base_tech_per_worker) / base_tech_per_worker) * 100, 2
)

for key, value in data.items():
    print(f"{key}: {value}")